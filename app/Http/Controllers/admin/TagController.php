    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $where = [];
        if (\request('id')){
            $where[] = ['id','=',\request('id')];
        }
        if (\request('name')){
            $where[] = ['title','Like','%'.\request('name').'%'];
        }
        if (\request('slug')){
            $where[] = ['slug','Like','%'.\request('slug').'%'];
        }

        if (count($where) > 0)
            $tags = Tag::where($where)->orderBy('id', 'desc')->paginate(10)->withQueryString();
        else
            $tags = Tag::orderBy('id', 'desc')->paginate(10)->withQueryString();

        return view(
            'admin.tags',
            [
                'tags'=>$tags,
                'sidebar'=>'tags'
            ]
        );
    }

